## JAVA UNIT TESTING JUnit5.

Testing -> Last step of SDLC where a dedicated team of quality Assurance, Testing manullay test each function of entire application after its complete development.

unit Testing -> In simple term we break our code file class in small units and test their behaviour and functions. This is done during development of software by its developer.Testing is done at several points to confirm proper function time to time.

Basic step to do unit test manually, but may change based on type of unit/code bloce we are working on.

1. Prepare, set up environment write methods.
2. Provide testing input.
3. Run the test.
4. Provide expected result.
5. Perform Assertion, verify the result.
6. Report Test result to the developer.

JUnit5 will auto done the point 3, 5, & 6.